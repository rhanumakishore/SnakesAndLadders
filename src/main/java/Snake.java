package main.java;

public class Snake {
	
	int snakeStartPoint;
	int snakeEndPoint;
	String snakeHungerLevel;
	
	
	public int getSnakeStartPoint() {
		return snakeStartPoint;
	}
	public void setSnakeStartPoint(int snakeStartPoint) {
		this.snakeStartPoint = snakeStartPoint;
	}
	public int getSnakeEndPoint() {
		return snakeEndPoint;
	}
	public void setSnakeEndPoint(int snakeEndPoint) {
		this.snakeEndPoint = snakeEndPoint;
	}
	public String getSnakeHungerLevel() {
		return snakeHungerLevel;
	}
	public void setSnakeHungerLevel(String snakeHungerLevel) {
		this.snakeHungerLevel = snakeHungerLevel;
	}
	
	

}
